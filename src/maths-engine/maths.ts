export function difference(a: number, b: number) {
  return console.log(a - b);
}

export function sum(a: number, b: number) {
  return console.log(a + b);
}

export function multi(a: number, b: number) {
  return console.log(a * b);
}

export function divi(a: number, b: number) {
  return console.log(a / b);
}

export function mathPow(a: number, b: number) {
  return console.log(Math.pow(a, b));
}

export function mathRandom() {
  return console.log(Math.random());
}
